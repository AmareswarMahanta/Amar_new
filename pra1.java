package practi;

public class pra1 {

	private int id;
	private String name;
	public int age;

	// Setters(mutators)
	public void setId(int ids) {
		this.id = ids;
	}

	public void setName(String names) {
		this.name = names;
	}

	public void setAge(int ages) {
		this.age = ages;
	}

	// getter(accessers)
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

}
